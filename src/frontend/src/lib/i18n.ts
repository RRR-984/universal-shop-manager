export const RTL_LANGUAGES = new Set<Language>([
  Language.Arabic,
  Language.Urdu,
]);

export function isRTL(lang: Language): boolean {
  return RTL_LANGUAGES.has(lang);
}

export function applyRTL(lang: Language): void {
  const dir = isRTL(lang) ? "rtl" : "ltr";
  document.documentElement.setAttribute("dir", dir);
  document.documentElement.setAttribute("lang", getLangCode(lang));
}

function getLangCode(lang: Language): string {
  const codes: Record<Language, string> = {
    [Language.English]: "en",
    [Language.Hindi]: "hi",
    [Language.Arabic]: "ar",
    [Language.French]: "fr",
    [Language.Spanish]: "es",
    [Language.Portuguese]: "pt",
    [Language.Swahili]: "sw",
    [Language.BahasaIndonesia]: "id",
    [Language.Bengali]: "bn",
    [Language.Urdu]: "ur",
  };
  return codes[lang] ?? "en";
}

let currentLanguage: Language = Language.English;

export function setLanguage(lang: Language): void {
  currentLanguage = lang;
  applyRTL(lang);
}

export function getCurrentLanguage(): Language {
  return currentLanguage;
}

export function t(key: TranslationKey): string {
  return (
    translations[currentLanguage]?.[key] ??
    translations[Language.English][key] ??
    key
  );
}

export function useTranslation() {
  return { t, language: currentLanguage, isRTL: isRTL(currentLanguage) };
}

export type { TranslationKey };
